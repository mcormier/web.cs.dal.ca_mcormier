<a name="interface_design"><h3>Interface Design - Reading text in a second language</h3></a>
<B>Description</B>
<BR>
<P>Human Computer Interaction is a fourth year computer science course taught
at Dalhousie University.  Part of the requirements for this course was to choose "a novel 
user interface or interaction technique", then prototype and research it.  

I chose to design an interface that would aid someone in reading a second language.
My goal was to design an interface that allows a user to look up
word definitions seamlessly, and therefore minimize the interruption of reading.
This idea came from personal experience of flipping through many French-English, English,
and French dictionaries when reading French.
</P>
<BR>
<B>Requirements</b>
<OL>
	<LI> Prototype proposal
	<LI> Video prototype
	<LI> Interactive software prototype
	<LI> Final Report
</OL>
