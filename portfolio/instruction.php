<a name="ESL"><h3>ESL (English as a Second Language) tutor </h3></a>
<B>Description</B>
<BR>

<p>Dalhousie University hosts many foreign exchange students, and the Computer Science
faculty contains an above average amount of exchange students.  Because of
the high demand for ESL instruction in the Computer Science faculty, a peer ESL
tutoring program was established.
</p>

<p>I volunteered to be an ESL tutor and was assigned two student peers, 
both studying at the graduate level.  Instruction included a group meeting (once a
week), and an individual meeting with each student (also once a week).  Group meetings were
formatted to provide english social interaction in a relaxed setting, usually with the use of 
games.  Individual meetings concentrated on pronounciation and comprehension (reading 
newspapers, tongue twisters).
</p>

<B>Related Skills</b>
<UL>
	<LI>Organization and prepration
	<LI>Listening
	<LI>Patience and Humility
</UL>

<p>
Having been through a French immersion program and previous experience as an exchange student 
helped me to empathize with ESL students. I greatly enjoyed this work and plan to do similar
work in the future.
</P>
