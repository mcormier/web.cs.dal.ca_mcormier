<a name="TA"><h3>Teacher's Assistant - Computer Science Learning Centre </h3></a>
<B>Description</B>
<BR>
<p>
Traditionally there is a high failure rate for first year computer programming
classes.  
To combat that high failure rate, 
help new computer users become familiar with the local computing environment 
and overcome common programming stumbling blocks,
a <a href="http://www.cs.dal.ca/studentservices/learning.shtml" target="_blank">Learning Centre</a>
was established. 
</p><p>
Since other faculties may provide the choice of either calculus 
or computer programming as a degree requirement,
there is an eclectic mix of people with varying familiarity and goals in the first
year classes.  Regularly helping a broad spectrum of people with rudimentary to complex
problems was enjoyable; teaching students (who are under pressure) concepts instead of 
handing them answers was challenging.
</p>


