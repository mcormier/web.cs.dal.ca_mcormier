<BR>
<p> The Learning Centre commonly provides help for 1st and 2nd year undergraduates but is
open to anyone who has a problem and asks for help. I worked at the Learning Centre for two consecutive 
semesters and aided students with a wide range of problems and technologies including: Java, C, 
Assembly Language, data structures, Javascript, HTML, C++, Unix, SQL.
</p>
